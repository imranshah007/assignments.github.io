num = parseInt(num);

if(num < 10)
{
    console.log("Number is less than 10");
    alert("Number is less than 10");
}
else if(num > 10)
{
    console.log("Number is greater than 10");
    alert("Number is less greater 10");
}
else
{
    console.log("Number is equal to 10");
    alert("Number is equal to 10");
}